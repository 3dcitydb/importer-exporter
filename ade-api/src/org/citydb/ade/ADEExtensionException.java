package org.citydb.ade;

public class ADEExtensionException extends Exception {
	private static final long serialVersionUID = -3716015045363231263L;
	
	public ADEExtensionException() {
		super();
	}
	
	public ADEExtensionException(String message) {
		super(message);
	}
	
	public ADEExtensionException(Throwable cause) {
		super(cause);
	}
	
	public ADEExtensionException(String message, Throwable cause) {
		super(message, cause);
	}
	
}
